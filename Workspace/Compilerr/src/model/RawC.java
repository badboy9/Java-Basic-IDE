package model;

public class RawC {

	private String code;
	private String output;
	public RawC()
	{
		code="";
		output="";
	}
	public String getCode()
	{
		return code;
	}
	public String getOutput()
	{
		return output;
	}	
	public void setCode(String obj)
	{
		code=obj;
	}
	public void setOutput(String obj)
	{
		output=obj;
	}
}
